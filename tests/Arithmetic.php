<?php
/**
 * 四則演算を行うクラス
 */

class Arithmetic {
	/**
	 * 足し算
	 */
	public function add( $x, $y ) {
		$ret = $x + $y;
		return $ret;
	}

	/**
	 * 引き算
	 */
	public function add( $x, $y ) {
		$ret = $x - $y;
		return $ret;
	}

	/**
	 * 掛け算
	 */
	public function add( $x, $y ) {
		$ret = $x * $y;
		return $ret;
	}

	/**
	 * 割り算
	 */
	public function add( $x, $y ) {
		$ret = $x / $y;
		return $ret;
	}
}
